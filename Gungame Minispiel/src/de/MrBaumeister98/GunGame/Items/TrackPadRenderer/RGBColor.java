package de.MrBaumeister98.GunGame.Items.TrackPadRenderer;

class RGBColor {
	
	private int R;
	private int G;
	private int B;
	
	public RGBColor(int r, int g, int b) {
		this.R = r;
		this.G = g;
		this.B = b;
	}
	
	public int getR() {
		return this.R;
	}
	public int getG() {
		return this.G;
	}
	public int getB() {
		return this.B;
	}

}
